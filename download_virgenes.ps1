$outputDir = "d:\Antigravity\MARIALUX\virgenes"
$total = $urls.Count
$downloaded = 0
$failed = 0
$failedList = @()

Write-Host "Starting download of $total advocacion images..." -ForegroundColor Cyan
Write-Host "Using og:image meta tag for reliable image extraction" -ForegroundColor Gray
Write-Host ""

foreach ($i in 0..($urls.Count - 1)) {
    $url = $urls[$i]
    $slug = ($url -replace 'https://www.wikitolica.com/[a-z]/', '' -replace '/$', '')
    $num = $i + 1
    Write-Host "[$num/$total] $slug " -ForegroundColor Yellow -NoNewline
    
    try {
        $response = Invoke-WebRequest -Uri $url -UseBasicParsing -TimeoutSec 15
        $html = $response.Content
        
        # Extract og:image meta tag - most reliable source
        $imageUrl = $null
        $ogMatch = [regex]::Match($html, 'content=(https://www\.wikitolica\.com/img/[^\s>]+\.webp)\s+property=og:image')
        if ($ogMatch.Success) {
            $imageUrl = $ogMatch.Groups[1].Value
        }
        
        # Fallback: try standard og:image format
        if (-not $imageUrl) {
            $ogMatch2 = [regex]::Match($html, 'property=og:image\s+content="?([^"\s>]+)"?')
            if ($ogMatch2.Success) {
                $imageUrl = $ogMatch2.Groups[1].Value
            }
        }
        
        # Fallback: look for figure > img pattern
        if (-not $imageUrl) {
            $figMatch = [regex]::Match($html, '<figure[^>]*>\s*<img[^>]+src=([^\s>]+)')
            if ($figMatch.Success) {
                $src = $figMatch.Groups[1].Value -replace '"', ''
                if ($src -match '^/') {
                    $imageUrl = "https://www.wikitolica.com$src"
                }
                else {
                    $imageUrl = $src
                }
            }
        }
        
        if ($imageUrl) {
            $ext = ".webp"
            if ($imageUrl -match '\.(jpg|jpeg|png|webp)') {
                $ext = "." + $Matches[1]
            }
            
            $filename = "$slug$ext"
            $filepath = Join-Path $outputDir $filename
            
            Invoke-WebRequest -Uri $imageUrl -OutFile $filepath -UseBasicParsing -TimeoutSec 30
            $fileSize = (Get-Item $filepath).Length
            $sizeKB = [math]::Round($fileSize / 1024, 1)
            $downloaded++
            Write-Host "OK (${sizeKB}KB)" -ForegroundColor Green
        }
        else {
            $failed++
            $failedList += $slug
            Write-Host "NO IMAGE" -ForegroundColor Red
        }
    }
    catch {
        $failed++
        $failedList += "$slug (ERROR: $($_.Exception.Message))"
        Write-Host "ERROR" -ForegroundColor Red
    }
    
    # Small delay to be respectful
    Start-Sleep -Milliseconds 300
}

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "  DOWNLOAD COMPLETE" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "  Downloaded: $downloaded / $total" -ForegroundColor Green
Write-Host "  Failed:     $failed / $total" -ForegroundColor $(if ($failed -gt 0) { "Red" } else { "Green" })
if ($failedList.Count -gt 0) {
    Write-Host ""
    Write-Host "  Failed items:" -ForegroundColor Red
    foreach ($f in $failedList) {
        Write-Host "    - $f" -ForegroundColor Red
    }
}
Write-Host "========================================" -ForegroundColor Cyan
